    <script>
        $('#form').submit((e) => {
            e.preventDefault();

            var $form = $('#form');

            if ($form.parsley().validate()) {
                let url = "{{ url('login_user') }}";
                $.ajax({ 
                    url: url,
                    data: $form.serialize(),
                    type: 'post',
                    success: function(result) {
                        if (result.status == 200) {
                            console.log('success');
                        } else {
                            console.log('fail');
                        }
                    }
                });
            } else {
                console.log('Form is not validated');
            }
        });
